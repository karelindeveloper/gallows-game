// Word choice
if (window.location.toString().indexOf("game.html") > 0) {
  if (titleSpanID == "navRandom") {
    let titleSpanRandom =
      Object.keys(nav)[Math.floor(Math.random() * Object.keys(nav).length)];
    titleSpanID = nav[titleSpanRandom];
  } else {
    titleSpanID = nav[titleSpanID];
  }

  let randomElementWord =
    titleSpanID[Math.floor(Math.random() * titleSpanID.length)].toUpperCase();

  // Word parsing
  const elemSectionWord = document.querySelector(".word");

  let wordLenght = randomElementWord.length;
  console.log(randomElementWord);

  for (let index = 0; index < wordLenght; index++) {
    elemSectionWord.innerHTML += "<div class='word__block'></div>";
  }

  const elemLetters = document.querySelectorAll(".letters__letter");
  const elemWords = document.querySelectorAll(".word__block");
  let countTrue = 0;

  for (const elemLetter of elemLetters) {
    elemLetter.addEventListener("click", () => {
      let textLetter = elemLetter.innerHTML.toUpperCase();
      let indexLetter = -1;
      while (
        (indexLetter = randomElementWord
          .split("")
          .indexOf(textLetter, indexLetter + 1)) != -1
      ) {
        if (!elemWords[indexLetter].innerHTML) {
          elemWords[indexLetter].innerHTML = textLetter;
          countTrue++;
          countTrue == wordLenght
            ? setTimeout(() => dialogShow(countError, randomElementWord), 500)
            : false;
        }
      }
      if (
        randomElementWord.split("").indexOf(textLetter) == -1 &&
        !elemLetter.classList.contains("false") &&
        !elemLetter.classList.contains("true")
      ) {
        elemLetter.classList.add("false");
        countError++;
        errorHanding(countError, randomElementWord);
      } else if (
        !elemLetter.classList.contains("false") &&
        !elemLetter.classList.contains("true")
      ) {
        elemLetter.classList.add("true");
      }
    });
  }
}

//  Error handling
const elemBlockImageGallows = document.querySelector(".image__block");

function errorHanding(countError, randomElementWord) {
  elemBlockImageGallows.innerHTML = `<img alt="Виселица" src="./images/gallows${countError}.png" class="image__item max-h-[40vh]" />`;

  countError == 7
    ? setTimeout(() => dialogShow(countError, randomElementWord), 500)
    : false;
}

